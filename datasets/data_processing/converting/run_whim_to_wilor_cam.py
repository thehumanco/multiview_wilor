#!/usr/bin/env python3
"""
Convert WHIM pinhole camera annotations (K, trans) into WiLoR weak-perspective
camera params pred_cam = (s, c_x, c_y), per hand. Closed-form, exact.

WHIM was generated by WiLoR on these (1920-wide) images, so the two cameras are
the SAME pinhole model: WiLoR's effective full-image focal,
    scaled_focal = FOCAL_LENGTH/IMAGE_SIZE * max(img_w, img_h) = 5000/256*1920 = 37500,
equals WHIM's K[0,0] = 37500. There is no model mismatch and no SGD: we simply
invert WiLoR's cam_crop_to_full (wilor/utils/renderer.py:12-22) given WHIM's
stored trans=(Tx,Ty,Tz) and the hand bbox.

cam_crop_to_full (the WiLoR forward we invert):
    box_size = rescale_factor(2.5) * max(box_w, box_h)   # square crop side
    bs  = box_size * s
    t_z = 2*scaled_focal / bs
    t_x = 2*(cx - img_w/2)/bs + c_x
    t_y = 2*(cy - img_h/2)/bs + c_y

Inversion:
    bs  = 2*scaled_focal / Tz
    s   = bs / box_size
    c_x = Tx - 2*(cx - img_w/2)/bs
    c_y = Ty - 2*(cy - img_h/2)/bs

The script verifies each conversion by running WiLoR's full forward projection
and comparing to WHIM's stored joints_2d (expect ~0 px).
"""
import argparse
import glob
import os

import numpy as np
import torch


# WiLoR fixed config (wilor/configs/__init__.py + models/__init__.py assert)
F_WILOR = 5000.0
IMG_WILOR = 256.0


def _as_np(x):
    """WHIM mixes numpy arrays and torch tensors inside the dicts."""
    if torch is not None and isinstance(x, torch.Tensor):
        return x.detach().cpu().numpy()
    return np.asarray(x)


def convert_hand(vertices, joints_2d, bbox, K, trans, img_wh, rescale_factor=2.5):
    """Closed-form WHIM pinhole -> WiLoR (s, c_x, c_y).

    WHIM was generated by WiLoR on these images, so the two cameras are the SAME
    pinhole model: WiLoR's scaled_focal = FOCAL/IMG * max(img_w,img_h) equals
    WHIM's K[0,0]. So we invert cam_crop_to_full exactly (no SGD needed):

        cam_crop_to_full (renderer.py:12-22):
            bs  = box_size * s
            t_z = 2*scaled_focal / bs
            t_x = 2*(cx - img_w/2)/bs + c_x
            t_y = 2*(cy - img_h/2)/bs + c_y

    Inverting for (s, c_x, c_y) given WHIM trans=(Tx,Ty,Tz):
    """
    V = _as_np(vertices).astype(np.float64)
    j2d = _as_np(joints_2d).astype(np.float64)
    bbox = _as_np(bbox).astype(np.float64)
    K = _as_np(K).astype(np.float64)
    Tx, Ty, Tz = _as_np(trans).astype(np.float64)
    img_w, img_h = float(img_wh[0]), float(img_wh[1])

    # WiLoR's effective full-image focal (== WHIM K[0,0] by construction).
    scaled_focal = F_WILOR / IMG_WILOR * max(img_w, img_h)

    bx0, by0, bx1, by1 = bbox
    cx, cy = (bx0 + bx1) / 2.0, (by0 + by1) / 2.0
    # WiLoR crop: box_size = rescale_factor * max(box_w, box_h), squared.
    box_size = rescale_factor * max(bx1 - bx0, by1 - by0)

    bs = 2.0 * scaled_focal / Tz            # from t_z = 2*sf/bs
    s = bs / box_size                        # pred_cam scale
    c_x = Tx - 2.0 * (cx - img_w / 2.0) / bs
    c_y = Ty - 2.0 * (cy - img_h / 2.0) / bs

    # --- verify: run WiLoR's forward (cam_crop_to_full + project) and compare
    #     to WHIM's stored joints_2d. Should be ~0 px if the model matches.
    t_full = np.array([
        2.0 * (cx - img_w / 2.0) / bs + c_x,
        2.0 * (cy - img_h / 2.0) / bs + c_y,
        2.0 * scaled_focal / bs,
    ])
    Xc = V + t_full
    uv = scaled_focal * Xc[:, :2] / Xc[:, 2:3]
    uv[:, 0] += img_w / 2.0
    uv[:, 1] += img_h / 2.0
    px_err = float(np.abs(uv - j2d).mean())

    return {
        "s": float(s), "c_x": float(c_x), "c_y": float(c_y),
        "t_z": float(2.0 * scaled_focal / bs),
        "scaled_focal": float(scaled_focal), "f_whim": float(K[0, 0]),
        "reproj_px_err": px_err,
    }


def main():
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--anno_root", default="/ego_dataset/opensource/wilor/WHIM/train/anno")
    ap.add_argument("--npy", default=None, help="single .npy to test (overrides --anno_root sampling)")
    ap.add_argument("--img_w", type=float, default=None, help="image width (default: infer 2*cx from K)")
    ap.add_argument("--img_h", type=float, default=None, help="image height (default: infer 2*cy from K)")
    ap.add_argument("--rescale_factor", type=float, default=2.5)
    args = ap.parse_args()

    f = args.npy or sorted(glob.glob(os.path.join(args.anno_root, "*", "*.npy")))[0]
    print("file:", f)
    a = np.load(f, allow_pickle=True)

    for i, h in enumerate(a):
        K = _as_np(h["K"])
        # WHIM K's principal point = image center, so img size = 2*(cx,cy).
        img_w = args.img_w if args.img_w else 2.0 * float(K[0, 2])
        img_h = args.img_h if args.img_h else 2.0 * float(K[1, 2])
        out = convert_hand(h["vertices"], h["joints_2d"], h["bbox"], h["K"], h["trans"],
                           (img_w, img_h), rescale_factor=args.rescale_factor)
        print(f"--- entry {i} (side={float(h['side'])}) ---")
        print(f"  img={img_w:.0f}x{img_h:.0f}  scaled_focal={out['scaled_focal']:.1f}  "
              f"f_whim={out['f_whim']:.1f}")
        print(f"  pred_cam: s={out['s']:.4f}  c_x={out['c_x']:.4f}  c_y={out['c_y']:.4f}  "
              f"(t_z={out['t_z']:.4f})")
        print(f"  reproj error vs joints_2d: {out['reproj_px_err']:.4f} px")


if __name__ == "__main__":
    main()
